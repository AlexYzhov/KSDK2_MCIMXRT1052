//产生RGB屏的同步时钟
static void Display_SetClock(void) {
	clock_video_pll_config_t config = {
        .loopDivider = 36,
		.postDivider = 8,
		.numerator = 0,
		.denominator = 0,
    };

    CLOCK_InitVideoPll(&config);
	CLOCK_SetMux(kCLOCK_LcdifPreMux, 2);
	CLOCK_SetDiv(kCLOCK_LcdifPreDiv, 1);
	CLOCK_SetDiv(kCLOCK_LcdifDiv, 1);
}

void Display_Init(void) {
	const elcdif_rgb_mode_config_t config = {
		.panelWidth = DISPLAY_WIDTH,
		.panelHeight = DISPLAY_HEIGHT,
		.hsw = DSI_HSW,
		.hfp = DSI_HFP,
		.hbp = DSI_HBP,
		.vsw = DSI_VSW,
		.vfp = DSI_VFP,
		.vbp = DSI_VBP,
		.polarityFlags = DSI_POL_FLAGS,
		.bufferAddr = (uint32_t)activeFrameAddr,
		.pixelFormat = kELCDIF_PixelFormatRAW8,
		.dataBus = kELCDIF_DataBus8Bit,
	};

	//memset((uint8_t*)&dsi_frameBuffer[0][0], 0xA0, DISPLAY_WIDTH*DISPLAY_HEIGHT);
	//memset((uint8_t*)&dsi_frameBuffer[0][DISPLAY_WIDTH*DISPLAY_HEIGHT/2], 0x0A, DISPLAY_WIDTH*DISPLAY_HEIGHT);

	HX8369A_LCD_Init();

	BOARD_InitDSIPins();

	Display_SetClock();

	ELCDIF_RgbModeInit(LCDIF, &config);
	ELCDIF_RgbModeStart(LCDIF);
	ELCDIF_UpdateLut(LCDIF, kELCDIF_Lut0, 0, elcdif_gray2rgb565_lut, sizeof(elcdif_gray2rgb565_lut)/sizeof(elcdif_gray2rgb565_lut[0]));
	ELCDIF_UpdateLut(LCDIF, kELCDIF_Lut1, 0, elcdif_gray2rgb565_lut, sizeof(elcdif_gray2rgb565_lut)/sizeof(elcdif_gray2rgb565_lut[0]));
	ELCDIF_EnableLut(LCDIF, true);

	ELCDIF_EnableInterrupts(LCDIF, kELCDIF_CurFrameDoneInterruptEnable);
	EnableIRQ(LCDIF_IRQn);
}

void LCDIF_IRQHandler(void) {
	uint32_t status = ELCDIF_GetInterruptStatus(LCDIF);
	ELCDIF_ClearInterruptStatus(LCDIF, status);

	if(status&kELCDIF_CurFrameDone) {
		ELCDIF_SetNextBufferAddr(LCDIF, activeFrameAddr);
	}
}